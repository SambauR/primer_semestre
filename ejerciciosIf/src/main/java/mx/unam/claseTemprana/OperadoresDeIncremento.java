
package mx.unam.claseTemprana;

public class OperadoresDeIncremento {
    public static void main(String[] args) {
        int a = 3, b=3;
        
        System.out.println("el valor de a es "+a);
        System.out.println( ++a);
        System.out.println("el valor de a es "+a);
        System.out.println( a++);
        System.out.println("el valor de a es "+a);
        System.out.println( --a);
        System.out.println("el valor de a es "+a);
        System.out.println( a--);
        System.out.println("el valor de a es "+a);
    }
}
